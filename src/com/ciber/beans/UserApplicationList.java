package com.ciber.beans;

import java.io.Serializable;

public class UserApplicationList implements Serializable{
	private String appId;
	private String userappName;
	private String userappType;
	private String role;
	
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
	public String getUserappName() {
		return userappName;
	}
	public void setUserappName(String userappName) {
		this.userappName = userappName;
	}
	public String getUserappType() {
		return userappType;
	}
	public void setUserappType(String userappType) {
		this.userappType = userappType;
	}
	

}
